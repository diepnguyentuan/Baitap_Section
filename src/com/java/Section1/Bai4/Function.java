package com.java.Section1.Bai4;

public class Function {
    public static double canBac2(double a) {
        return Math.sqrt(a);
    }

    public static double binhPhuong2(double a) {
        return Math.pow(a, 2);
    }

    public static double PI(double a) {
        return Math.PI * a;
    }
}
